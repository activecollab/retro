declare(strict_types=1);

namespace ActiveCollab\Retro\UI\Common\Property\Trait;

trait WithLabelTrait
{
    private ?string $label = null;

    public function label(?string $label): static
    {
        $this->label = $label;

        return $this;
    }

    public function getLabel(): ?string
    {
        return $this->label;
    }
}
