declare(strict_types=1);

namespace ActiveCollab\Retro\UI\Common\Trait;

trait WithRequiredLabelTrait
{
    private string $label;

    public function label(string $label): static
    {
        $this->label = $label;

        return $this;
    }

    public function getLabel(): string
    {
        return $this->label;
    }
}
